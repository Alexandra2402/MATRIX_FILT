void    contrastFilter_custom(cv::Mat& _frame)
{
    /// --- Определение ядра и параметров ---
    int kernel_weights[3][3] =
    {
        {-3, -3, -3},
        {-3, 32, -3},
        {-3, -3, -3}
    };
    int kernel_sum = 8; /// Сумма элементов ядра для деления

    /// --- Создаем копию изображения для чтения ---
    /// Это важно, чтобы при свертке мы использовали оригинальные значения пикселей,
    /// а не те, которые уже были изменены на предыдущих шагах.

    cv::Mat src = _frame.clone();

    /// --- Проходим по каждому пикселю изображения ---
    /// Исключаем рамку шириной 1 пиксель, так как ядро 3x3 выходит за пределы.

    for (uint16_t y = 1; y < src.rows - 1; ++y)
    {
        for (uint16_t x = 1; x < src.cols - 1; ++x)
        {
            int32_t sum = 0;

            /// --- Применяем ядро свертки ---
            for (uint16_t ky = 0; ky < 3; ++ky)
            {
                for (uint16_t kx = 0; kx < 3; ++kx)
                {
                    /// Координаты пикселя в src, соответствующего элементу ядра
                    int pixelY = y + ky - 1; /// -1, потому что центр ядра (1,1) соответствует (y,x)
                    int pixelX = x + kx - 1;

                    /// Читаем значение пикселя из исходного изображения
                    int pixel_value = src.at<uchar>(pixelY, pixelX);

                    /// Умножаем на вес ядра и суммируем
                    sum += pixel_value * kernel_weights[ky][kx];
                }
            }

            /// --- Применяем обратное масштабирование (деление на сумму ядра) ---
            int32_t result_val = sum / kernel_sum;

            /// --- Нормализация (Clamping) к диапазону 0-255 ---
            /// Результат может быть отрицательным или больше 255.

            _frame.at<uchar>(y, x) = CLAMP255(result_val);

        }
    }
}
